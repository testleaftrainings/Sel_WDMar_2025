package week1.day2;

public class LearnElseIf {

	public static void main(String[] args) {
		int a=10;
		int b=10;
		// 10>10
		if(a<b) {
			System.out.println("a is smaller than b");
		}
		else if(a>b) {
			System.out.println("a is greater than b");
		}
		else {
			System.out.println("a is equal to b");
		}
		

	}

}