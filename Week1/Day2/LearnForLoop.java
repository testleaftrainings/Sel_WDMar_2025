package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		
		// to print numbers 1 to 5
		//starting number =1
		//ending number =5
		// increased by 1
		
		for(int i=-10;i<5;i++) {
			System.out.println(i);
		}
		System.out.println("----------------");
		
		//To print the numbers from 5 to 1     5 4 3 2 1
		//starting number=5
	    //ending number=1
		// decreased by 1
		
		//i=5  4   3   2   1 
		for(int i=5;i>=1;i--) {
			System.out.println(i);
		}
		

	}

}
